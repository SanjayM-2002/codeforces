#include <bits/stdc++.h>
using namespace std;
/* 
Link: https://codeforces.com/problemset/problem/1335/A
Name: Candies and Two Sisters
TC: O(1)
SC: O(1)
*/
int main () {
    int t;
    cin >> t;
    while (t--) {
        long long n;
        cin >> n;
        long long res = (n-1)/2;
        cout << res << endl;
    }
    return 0;
}